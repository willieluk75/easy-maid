export default function BottomTabBar() {
  const pathname = usePathname();

  // 在 worker detail 頁和 signup/signin 頁隱藏 tab bar
  const hidden = pathname.startsWith('/workers/') && pathname !== '/workers'
    || pathname === '/signin'
    || pathname === '/signup';

  if (hidden) return null;

  return (
    <nav className="fixed bottom-0 left-0 right-0 z-40 bg-white border-t border-[#dddddd] safe-area-bottom">
      <div className="max-w-lg mx-auto flex items-center justify-around h-14">
        {TABS.map(tab => {
          const active = pathname === tab.href || pathname.startsWith(tab.href + '/');
          return (
            <Link
              key={tab.href}
              href={tab.href}
              className={`flex flex-col items-center justify-center gap-0.5 flex-1 h-full transition-colors ${
                active ? 'text-[#ff385c]' : 'text-[#929292] hover:text-[#3f3f3f]'
              }`}
            >
              {tab.icon(active)}
              <span className="text-[10px] font-medium">{tab.label}</span>
            </Link>
          );
        })}
      </div>
    </nav>
  );
}
